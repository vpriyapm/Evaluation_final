x = 2
y = 3
z = 5

# Perform one calculation on itself
x -= 2 # Same as x = x - 2
y *= 3 # Same as y = y * 3
z /= 2 # Same as z = z / 2

print("x: " + str(x))
print("y: " + str(y))
print("z: " + str(z))

