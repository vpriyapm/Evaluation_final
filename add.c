#include <stdio.h>

int main() {
    int num1, num2, sum2;
    
    printf("Enter the first number: ");
    scanf("%d", &num1);
    
    printf("Enter the second number: ");
    scanf("%d", &num2);
    
    sum2 = num1 + num2;
  
    
    printf("The sum of %d and %d is %d.", num1, num2, sum2);
    
    
    return 0;
}
